/**
 * Type definitions
 */

export interface Message {
  id: string;
  role: 'user' | 'assistant';
  content: string;
  timestamp: Date;
  toolsUsed?: string[];
}

export interface Conversation {
  id: string;
  messages: Message[];
  mode: 'admin' | 'user';
  createdAt: Date;
}

export interface AuthState {
  isAuthenticated: boolean;
  isAdmin: boolean;
  token?: string;
}
